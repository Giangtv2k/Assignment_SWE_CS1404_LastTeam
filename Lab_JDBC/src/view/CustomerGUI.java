    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        // TODO add your handling code here:
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        addNew = false;
        int pos = tblCustomer.getSelectedRow();
        if (pos != -1) {
            Customer customer = customerModel.getCustomers().get(pos);
            txtCustomerCode.setEnabled(true);
            txtCustomerName.setEnabled(true);
            txtDoB.setEnabled(true);
            txtAddress.setEnabled(true);
            rbMale.setEnabled(true);
            rbFemale.setEnabled(true);
            txtCustomerCode.setText(customer.getCustomerID());
            txtCustomerCode.setEnabled(false);
            txtCustomerName.setText(customer.getCustomerName());
            txtDoB.setText(dateFormat.format(customer.getBirhDate()));
            txtAddress.setText(customer.getAddress());
            btnUpdate.setEnabled(true);
            btnDelete.setEnabled(true);
            btnReset.setEnabled(true);
            if (customer.isGender()) {
                rbMale.setSelected(true);
            } else {
                rbFemale.setSelected(true);
            }
        }
    }//GEN-LAST:event_tblCustomerMouseClicked

