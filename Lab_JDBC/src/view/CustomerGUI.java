    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(this, "Do you want to delete " + txtCustomerCode.getText(), "Question", JOptionPane.YES_NO_OPTION) == 0) {
            int count = customers.deleteCustomer(txtCustomerCode.getText());
            if (count > 0) {
                JOptionPane.showMessageDialog(this, "A customer has been deleted");
                int pos = tblCustomer.getSelectedRow();
                customerModel.getCustomers().removeElementAt(pos);
                starup();
            } else {
                JOptionPane.showMessageDialog(this, "Cannot delete!");
            }
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String customerID = txtCustomerCode.getText();
        String customerName = txtCustomerName.getText();
        String address = txtAddress.getText();
        boolean gender;
        if (rbMale.isSelected()) {
            gender = true;
        } else {
            gender = false;
        }
        Date birhDate = Date.valueOf(txtDoB.getText());
        Customer customer = new Customer(customerID, null, customerName, address, gender, birhDate);
        if (addNew == true) {
        } else {
            int count = customers.updateCustomer(customer, 2);
            if (count > 0) {
                JOptionPane.showMessageDialog(this, "A customer has been updated");
                int pos = tblCustomer.getSelectedRow();
                customerModel.getCustomers().set(pos, customer);
            }
        }
        starup();
    }//GEN-LAST:event_btnUpdateActionPerformed

