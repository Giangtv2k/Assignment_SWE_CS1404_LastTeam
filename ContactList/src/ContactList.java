    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        for (int i = 0; i < data.size(); i++) {
            Vector v = (Vector) data.get(i);
            if (txtSearch.getText().equalsIgnoreCase((String) v.get(0))) {
                JOptionPane.showMessageDialog(this, "The contact name is: " + v.get(1));
                return;
            }
            if (i == data.size() - 1 && !txtSearch.getText().equalsIgnoreCase((String) v.get(0))) {
                JOptionPane.showMessageDialog(this, "The contact not found.");
                return;
            }
        }
    }//GEN-LAST:event_btnSearchMouseClicked

    private void tblContactMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblContactMouseClicked
        int row = this.tblContact.getSelectedRow();
        this.txtId.setText((String) (tblContact.getValueAt(row, 0)));
        this.txtId.setEditable(false);
        this.txtFullname.setText((String) (tblContact.getValueAt(row, 1)));

        if (tblContact.getValueAt(row, 2).equals("Male")) {
            rbMale.setSelected(true);
        } else {
            rbFemale.setSelected(true);
        }

        this.txtEmail.setText((String) (tblContact.getValueAt(row, 3)));
        this.txtPhone.setText((String) (tblContact.getValueAt(row, 4)));
        addNew = false;
    }//GEN-LAST:event_tblContactMouseClicked

