    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        int pos = this.tblContact.getSelectedRow();
        if (pos >= 0) {
            String code = (String) (tblContact.getValueAt(pos, 0));
            if (JOptionPane.showConfirmDialog(this, "Delete the " + code + " contact ?", "Question", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                data.remove(pos);
                tblContact.updateUI();
                changed = true;
            }
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

    private boolean valid() {
        String s = "";
        if (true) {
            s = txtId.getText().trim().toUpperCase();
            txtId.setText(s);

            if (!s.matches("^\\d{6}$")) {
                JOptionPane.showMessageDialog(this, "ID is an integer with lenth 6 digits.");
                txtId.requestFocus();
                return false;
            }

            for (int i = 0; i < data.size(); i++) {
                Vector v = (Vector) data.get(i);
                if (s.equalsIgnoreCase((String) v.get(0))) {
                    JOptionPane.showMessageDialog(this, "ID duplicated.");
                    txtId.requestFocus();
                    return false;
                }
            }

            if (txtFullname.getText().length() == 0) {
                JOptionPane.showMessageDialog(this, "Fullname is required.");
                txtFullname.requestFocus();
                return false;
            }

            if (!txtFullname.getText().trim().matches("^\\D+$")) {
                JOptionPane.showMessageDialog(this, "Fullname is not contain digit.");
                txtFullname.requestFocus();
                return false;
            }

            String regex = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
            if (!txtEmail.getText().matches(regex)) {
                JOptionPane.showMessageDialog(this, "Email invalid. EX:thopn3@fpt.edu.vn");
                txtEmail.requestFocus();
                return false;
            }

            if (!txtPhone.getText().trim().matches("^\\d{10}$")) {
                JOptionPane.showMessageDialog(this, "Phone is an interger with lengrth 10 digits.");
                txtPhone.requestFocus();
                return false;
            }
        }
        return true;
    }

    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        if (!valid()) {
            return;
        }
        if (addNew == true) {
            String code = txtId.getText().trim();
            String name = txtFullname.getText().trim();

            String email = txtEmail.getText().trim();
            String phone = txtPhone.getText().trim();
            Vector v = new Vector();
            v.add(code);
            v.add(name);

            if (rbMale.isSelected() == true) {
                v.add("Male");
            } else {
                v.add("Female");
            }
            v.add(email);
            v.add(phone);
            data.add(v);
        } else {

        }
        tblContact.updateUI();
        changed = true;
    }//GEN-LAST:event_btnSaveMouseClicked

