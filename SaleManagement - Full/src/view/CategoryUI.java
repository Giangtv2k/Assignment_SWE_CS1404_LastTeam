    private void tblCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCategoryMouseClicked
        // TODO add your handling code here:
        addNew = false;
        int position = tblCategory.getSelectedRow();
        // Lay du lieu fill vao 3 JTextField
        Category category = categoryModel.getCategories().get(position);
        txtCategoryId.setText(category.getCategoryId());
        txtCategoryName.setText(category.getCategoryName());
        txtDescription.setText(category.getDescription());
        txtCategoryId.setEnabled(false);
    }//GEN-LAST:event_tblCategoryMouseClicked

    private boolean validCategory() {
        // Kiem tra hop le cho CategoryId
        if (!txtCategoryId.getText().matches("^C\\d{4}$")) {
            JOptionPane.showMessageDialog(this, txtCategoryId.getText() + " invalid. Ex: C0000");
            txtCategoryId.requestFocus();
            return false;
        }

        // Kiem tra neu Category da ton tai duoi CSDL
        return true;
    }

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String catId = txtCategoryId.getText().trim();
        String catName = txtCategoryName.getText().trim();
        String desc = txtDescription.getText().trim();

        // Khai bao 1 doi tuong kieu Category
        Category category = new Category(catId, catName, desc);

        if (addNew == true) { // Thuc hien hoat dong them moi du lieu vao bang Categories
            // Kiem tra tinh hop le
            if (!validCategory()) {
                return;
            }

            int count = categories.addUpdateCategory(category, 1);
            if (count > 0) {
                JOptionPane.showMessageDialog(this, "A category has been added.");
                categoryModel.getCategories().add(category);
            }
        } else { // Tien hanh cap nhat du lieu vao bang Categories
            int count = categories.addUpdateCategory(category, 2);
            if (count > 0) {
                JOptionPane.showMessageDialog(this, "A category has been updated.");
                int pos = tblCategory.getSelectedRow();
                categoryModel.getCategories().set(pos, category);
            }
        }
        // Refresh lai tblCategory
        tblCategory.updateUI();
    }//GEN-LAST:event_btnSaveActionPerformed

