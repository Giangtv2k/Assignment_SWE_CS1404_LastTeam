    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        //lấy dòng mà người dùng click chọn trên table
        int row = tblCategory.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a Category to delete.", "Warning!", JOptionPane.ERROR_MESSAGE);
        } else {
            //lấy CategoryID
            String catId = (String) tblCategory.getValueAt(row, 0);
            if (JOptionPane.showConfirmDialog(this, "Delete the " + catId + " category?", "Question", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                //Thực hiện xóa dữ liệu trong database
                if (categories.deleteCategory(catId) > 0) {
                    JOptionPane.showMessageDialog(this, "A category has been deleted.");
                    //update lại categoryModel và tblCategory
                    categoryModel.getCategories().removeElementAt(row);
                    tblCategory.updateUI();
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchVal = txtSearch.getText();
        if (searchVal.length() == 0) {
            JOptionPane.showMessageDialog(this, "Enter the category name to search");
            txtSearch.requestFocus();
            loadCategories();
            tblCategory.updateUI();
        } else {
            categories = new Categories();
            categories.getCategoryByName(searchVal);
            if (categories.size() > 0) {
                categoryModel = new CategoryModel(categories);
                tblCategory.setModel(categoryModel);
                tblCategory.updateUI();
            } else {
                JOptionPane.showMessageDialog(this, "The category not found.");
                txtSearch.requestFocus();
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

